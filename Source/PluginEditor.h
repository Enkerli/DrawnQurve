/**
 * Shared visual metrics for the editor's three modal overlays:
 *   `_scaleOverlay`, `_routingOverlay`, `helpOverlay`.
 * T2.8 (visual-audit-2026-04 §4): one elevated-panel treatment — same
 * radius, border, soft shadow, and dim-backdrop opacity — so all three
 * read as the same kind of object.  Single source of truth lets future
 * tuning ripple through one place.
 *
 * The helpers `paintBackdrop()` / `paintPanel()` keep each overlay's
 * paint() down to two calls plus its own content rendering.
 */
namespace dcui::OverlayChrome
{
    /** Panel corner radius — splits the difference between the previous
     *  10 px (RoutingOverlay) and 12 px (ScaleOverlay) treatments. */
    inline constexpr float kPanelRadius      = 11.0f;

    /** Hairline panel border. */
    inline constexpr float kPanelBorderWidth = 1.0f;

    /** Soft drop-shadow blur radius (px). */
    inline constexpr int   kShadowRadius     = 14;

    /** Dim-backdrop alpha (0–255).  Light mode lifts the panel against a
     *  cream editor; dark mode needs a heavier dim against deep slate. */
    inline constexpr juce::uint8 kBackdropAlphaLight = 0x60;
    inline constexpr juce::uint8 kBackdropAlphaDark  = 0x80;

    inline juce::Colour panelBg     (bool light) noexcept
    { return light ? juce::Colour (0xfffaf8f5) : juce::Colour (0xff1e1e2a); }

    inline juce::Colour panelBorder (bool light) noexcept
    { return light ? juce::Colour (0xffccc8c0) : juce::Colour (0xff3a3a4a); }

    inline juce::Colour shadowCol   (bool light) noexcept
    { return juce::Colour::fromRGBA (0, 0, 0, light ? juce::uint8 (0x55) : juce::uint8 (0x88)); }

    inline juce::Colour backdrop    (bool light) noexcept
    {
        const juce::uint8 a = light ? kBackdropAlphaLight : kBackdropAlphaDark;
        return juce::Colour::fromRGBA (0, 0, 0, a);
    }

    /** Fills `fullArea` with the dim-backdrop colour. */
    inline void paintBackdrop (juce::Graphics& g, bool light, juce::Rectangle<int> fullArea)
    {
        g.setColour (backdrop (light));
        g.fillRect (fullArea);
    }

    /** Paints a soft shadow + filled panel + border at `panel`.
     *  No-op for empty rects (so overlays can call this unconditionally). */
    inline void paintPanel (juce::Graphics& g, bool light, juce::Rectangle<float> panel)
    {
        if (panel.isEmpty()) return;

        // Soft drop-shadow lifts the panel off the dim backdrop.
        juce::DropShadow (shadowCol (light), kShadowRadius, juce::Point<int> (0, 2))
            .drawForRectangle (g, panel.toNearestInt());

        g.setColour (panelBg (light));
        g.fillRoundedRectangle (panel, kPanelRadius);

        g.setColour (panelBorder (light));
        g.drawRoundedRectangle (panel.reduced (0.5f), kPanelRadius, kPanelBorderWidth);
    }
}

