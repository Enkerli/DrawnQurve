/**
 * App-level LookAndFeel — delivers the v2 Studio typography (Domine serif,
 * Inter Tight sans, Caveat hand) bundled as JUCE BinaryData.  Routing happens
 * inside getTypefaceForFont() based on a sentinel name embedded in
 * FontOptions::withName() by the makeXxxFont() factories below:
 *
 *   "DC-Serif"        → Domine.ttf            (italic synthesised by JUCE)
 *   "DC-Sans"         → InterTight.ttf
 *   "DC-Sans-Italic"  → InterTight-Italic.ttf
 *   "DC-Hand"         → Caveat.ttf
 *
 * Anything else (the JUCE default sans-serif name, third-party widgets, etc.)
 * falls through to InterTight so the whole editor reads as v2 Studio.
 *
 * Each typeface is loaded once, lazily, and cached for process lifetime.
 *
 * All widget-specific LookAndFeel subclasses inside DrawnCurveEditor inherit
 * from this class so they all get the same font routing.
 */
class DrawnCurveLookAndFeel : public juce::LookAndFeel_V4
{
public:
    // ── Sentinel font names (used by FontOptions::withName + routed below) ──
    static constexpr const char* kSerifName       = "DC-Serif";
    static constexpr const char* kSansName        = "DC-Sans";
    static constexpr const char* kSansItalicName  = "DC-Sans-Italic";
    static constexpr const char* kHandName        = "DC-Hand";

    // ── T2.9 (visual-audit-2026-04 §4): single source of truth for UI palette ──
    /** Returns the palette for the current mode.  Replaces inline
     *  `_lightMode ? Colour(0x…) : Colour(0x…)` ternaries scattered throughout
     *  PluginEditor.cpp.  Callers cache the const-ref locally and read the
     *  named tokens (e.g. `t.btnBg`, `t.dimText`).  Defined in
     *  PluginEditor.cpp where the kLight/kDark literals live. */
    static const Theme& theme (bool light) noexcept;

    DrawnCurveLookAndFeel()
    {
        // Make Inter Tight the implicit default so any LookAndFeel-wide query
        // for the sans-serif family also routes to our bundled typeface.
        setDefaultSansSerifTypefaceName (kSansName);
    }

    /** Route font requests to one of the four bundled typefaces based on the
     *  sentinel name set by makeXxxFont().  Style flags (italic / bold) are
     *  applied by JUCE on top of the returned typeface; for Domine (no italic
     *  master) this synthesises italic by skewing — matching the webapp, which
     *  also lacks a true Domine italic. */
    juce::Typeface::Ptr getTypefaceForFont (const juce::Font& f) override
    {
        const auto name = f.getTypefaceName();

        if (name == kSerifName)        return serifTypeface();
        if (name == kHandName)         return handTypeface();
        if (name == kSansItalicName)   return sansItalicTypeface();
        if (name == kSansName)         return sansTypeface();

        // JUCE default sans-serif requests + everything else → Inter Tight.
        return sansTypeface();
    }

    juce::Font getLabelFont (juce::Label&) override
    {
        return makeSansFont (12.0f);
    }

    juce::Font getTextButtonFont (juce::TextButton&, int buttonHeight) override
    {
        return makeSansFont (juce::jmin (14.0f, (float) buttonHeight * 0.55f));
    }

    juce::Font getComboBoxFont (juce::ComboBox&) override
    {
        return makeSansFont (13.0f);
    }

    /** T1.2 (visual-audit-2026-04 §3 P6): bump the slider thumb radius so
     *  performance-hot range / speed sliders are easier to grab on iPad.
     *  JUCE's default caps at 12 → 24 px diameter; we raise the floor to 14
     *  → 28 px diameter, still constrained by the slider's own height so
     *  shorter sliders elsewhere aren't overrun.  */
    int getSliderThumbRadius (juce::Slider& slider) override
    {
        const int axisExtent = slider.isHorizontal() ? slider.getHeight()
                                                     : slider.getWidth();
        return juce::jmin (16, axisExtent / 2);
    }

    // ── Font factories ──────────────────────────────────────────────────────
    //
    // Use these everywhere a juce::Font is needed.  They set a sentinel name
    // on FontOptions which getTypefaceForFont() routes to a bundled .ttf.
    //
    // We deliberately do NOT call FontOptions::withTypeface() here:
    //   • withTypeface embeds a size-0 typeface directly into the Font, which
    //     bypasses JUCE's per-render sizing and breaks Unicode shaping.
    //   • A later fallback call to withTypeface() on the same FontOptions
    //     fires jassert(x == nullptr || style.isEmpty()) in juce_FontOptions.h.
    // withFallbackEnabled is set so CoreText substitutes any code points the
    // bundled font delegates (emoji, rare scripts, etc.).

    /** Sans body — Inter Tight (regular or true italic). */
    static juce::Font makeSansFont (float height, bool italic = false)
    {
        return juce::Font (juce::FontOptions{}
                               .withName (italic ? kSansItalicName : kSansName)
                               .withHeight (height)
                               .withFallbackEnabled (true));
    }

    /** Serif accent — Domine.  Italic is synthesised by JUCE (no italic
     *  master in the .ttf, matching the webapp). */
    static juce::Font makeSerifFont (float height, bool italic = false)
    {
        auto f = juce::Font (juce::FontOptions{}
                                 .withName (kSerifName)
                                 .withHeight (height)
                                 .withFallbackEnabled (true));
        return italic ? f.italicised() : f;
    }

    /** Handwriting — Caveat (used sparingly: hint text, captures, etc.). */
    static juce::Font makeHandFont (float height)
    {
        return juce::Font (juce::FontOptions{}
                               .withName (kHandName)
                               .withHeight (height)
                               .withFallbackEnabled (true));
    }

    /** Backward-compat alias — old call sites that requested "the" font
     *  (sans, regular weight) still work without modification. */
    static juce::Font makeFont (float height) { return makeSansFont (height); }

private:
    // ── Process-lifetime typeface cache ─────────────────────────────────────
    //
    // Each typeface is created from BinaryData on first request and held for
    // the lifetime of the process.  Safe inside the AUv3 XPC sandbox because
    // BinaryData lives in the plugin binary itself (no filesystem access).

    static const juce::Typeface::Ptr& serifTypeface()
    {
        static const juce::Typeface::Ptr instance =
            juce::Typeface::createSystemTypefaceFor (BinaryData::Domine_ttf,
                                                     BinaryData::Domine_ttfSize);
        return instance;
    }

    static const juce::Typeface::Ptr& sansTypeface()
    {
        static const juce::Typeface::Ptr instance =
            juce::Typeface::createSystemTypefaceFor (BinaryData::InterTight_ttf,
                                                     BinaryData::InterTight_ttfSize);
        return instance;
    }

    static const juce::Typeface::Ptr& sansItalicTypeface()
    {
        // juce_add_binary_data strips '-' from filenames (and converts '.' → '_'):
        // InterTight-Italic.ttf  →  BinaryData::InterTightItalic_ttf
        static const juce::Typeface::Ptr instance =
            juce::Typeface::createSystemTypefaceFor (BinaryData::InterTightItalic_ttf,
                                                     BinaryData::InterTightItalic_ttfSize);
        return instance;
    }

    static const juce::Typeface::Ptr& handTypeface()
    {
        static const juce::Typeface::Ptr instance =
            juce::Typeface::createSystemTypefaceFor (BinaryData::Caveat_ttf,
                                                     BinaryData::Caveat_ttfSize);
        return instance;
    }
};

